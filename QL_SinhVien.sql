-- 1. Danh mục Giảng viên
CREATE TABLE GiangVien (
    MaGV VARCHAR(20) PRIMARY KEY,
    HoTen NVARCHAR(100) NOT NULL
);

-- 2. Danh mục Môn học
CREATE TABLE MonHoc (
    MaMH VARCHAR(20) PRIMARY KEY,
    TenMH NVARCHAR(100) NOT NULL
);

-- 3. Danh mục Sinh viên
CREATE TABLE SinhVien (
    MSSV VARCHAR(20) PRIMARY KEY,
    HoTen NVARCHAR(100) NOT NULL,
    NgaySinh DATE NOT NULL
);

-- 4. Quản lý Lớp học
CREATE TABLE Lop (
    MaLop VARCHAR(20) PRIMARY KEY,
    MaMH VARCHAR(20) NOT NULL,
    MaGV VARCHAR(20) NOT NULL,
    HocKy INT NOT NULL,
    NamHoc VARCHAR(20) NOT NULL,
    TrongSoCC INT NOT NULL,
    TrongSoBT INT NOT NULL,
    TrongSoThi INT NOT NULL,
    TrangThaiKhoa BIT NOT NULL DEFAULT 0,
    FOREIGN KEY (MaMH) REFERENCES MonHoc(MaMH),
    FOREIGN KEY (MaGV) REFERENCES GiangVien(MaGV),
    CONSTRAINT CHK_TongTrongSo CHECK (TrongSoCC + TrongSoBT + TrongSoThi = 100)
);

-- 5. Quản lý Điểm
CREATE TABLE Diem (
    MaLop VARCHAR(20) NOT NULL,
    MSSV VARCHAR(20) NOT NULL,
    DiemCC DECIMAL(4,2) CHECK (DiemCC BETWEEN 0 AND 10),
    DiemBT DECIMAL(4,2) CHECK (DiemBT BETWEEN 0 AND 10),
    DiemThi DECIMAL(4,2) CHECK (DiemThi BETWEEN 0 AND 10),
    DiemTB DECIMAL(4,2) CHECK (DiemTB BETWEEN 0 AND 10),
    PRIMARY KEY (MaLop, MSSV),
    FOREIGN KEY (MaLop) REFERENCES Lop(MaLop),
    FOREIGN KEY (MSSV) REFERENCES SinhVien(MSSV)
);
GO

